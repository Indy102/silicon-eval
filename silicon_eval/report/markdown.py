"""Markdown rendering of reports: one comparison table plus context notes."""

from __future__ import annotations

from pathlib import Path

from silicon_eval.report.schema import Report, VariantResult


def render_markdown(report: Report) -> str:
    """Report as a Markdown document: context, comparison table, notes."""
    lines = ["# silicon-eval report", ""]
    lines.extend(_context_lines(report))
    lines.append("")
    lines.extend(_comparison_table(report.variants))
    notes = _notes(report.variants)
    if notes:
        lines.extend(["", "## Notes", ""])
        lines.extend(f"- {note}" for note in notes)
    return "\n".join(lines) + "\n"


def write_report_markdown(report: Report, path: Path) -> None:
    """Render ``report`` to ``path`` as Markdown."""
    path.write_text(render_markdown(report), encoding="utf-8")


def _context_lines(report: Report) -> list[str]:
    machine = report.machine
    models = sorted({variant.model_id for variant in report.variants})
    memory_gb = machine.memory_bytes / 1024**3
    return [
        f"**Model:** {', '.join(models) if models else 'n/a'}",
        "",
        f"**Machine:** {machine.chip or 'unknown chip'}, {memory_gb:.0f} GB unified memory, "
        f"{machine.os_version}, Python {machine.python_version}",
        "",
        f"*Generated by silicon-eval {report.silicon_eval_version} "
        f"(schema v{report.schema_version}) at {report.created_at}.*",
    ]


def _comparison_table(variants: list[VariantResult]) -> list[str]:
    header = [
        "quant",
        "runtime",
        "ppl (wikitext2)",
        "hellaswag acc_norm",
        "mmlu acc",
        "ttft (s)",
        "gen tok/s",
        "prompt tok/s",
        "peak metal",
        "peak rss",
        "energy (mJ/tok)",
    ]
    rows = [_variant_row(variant) for variant in variants]
    widths = [len(cell) for cell in header]
    for row in rows:
        widths = [max(width, len(cell)) for width, cell in zip(widths, row, strict=True)]

    def fmt(cells: list[str]) -> str:
        padded = (cell.ljust(width) for cell, width in zip(cells, widths, strict=True))
        return "| " + " | ".join(padded) + " |"

    divider = "|" + "|".join("-" * (width + 2) for width in widths) + "|"
    return [fmt(header), divider] + [fmt(row) for row in rows]


def _variant_row(variant: VariantResult) -> list[str]:
    generation = variant.generation
    ppl = _metric(variant, "perplexity:wikitext2", "perplexity")
    accuracy_norm = _metric(variant, "hellaswag", "accuracy_norm")
    mmlu_accuracy = _metric(variant, "mmlu", "accuracy")
    energy = variant.energy
    return [
        variant.quantization.value,
        variant.runtime,
        f"{ppl:.2f}" if ppl is not None else "n/a",
        f"{accuracy_norm:.3f}" if accuracy_norm is not None else "n/a",
        f"{mmlu_accuracy:.3f}" if mmlu_accuracy is not None else "n/a",
        f"{generation.ttft_s.mean:.3f}",
        f"{generation.generation_tps.mean:.1f}",
        f"{generation.prompt_tps.mean:.1f}",
        _mb(generation.peak_metal_bytes),
        _mb(variant.peak_rss_bytes),
        f"{energy.energy_per_generated_token_mj:.1f}" if energy is not None else "n/a",
    ]


def _notes(variants: list[VariantResult]) -> list[str]:
    notes: list[str] = []
    first = variants[0] if variants else None
    if first is not None:
        scored = _metric(first, "perplexity:wikitext2", "scored_tokens")
        if scored is not None:
            notes.append(
                f"Perplexity over the first {scored:.0f} tokens of WikiText-2 (raw, test); "
                "all variants score the identical prefix. Not comparable to published "
                "sliding-window numbers."
            )
        items = _metric(first, "hellaswag", "items")
        if items is not None:
            notes.append(
                f"HellaSwag accuracy over the first {items:.0f} validation items "
                "(length-normalized log-likelihood scoring)."
            )
        mmlu_items = _metric(first, "mmlu", "items")
        if mmlu_items is not None:
            notes.append(
                f"MMLU accuracy over the first {mmlu_items:.0f} test items "
                "(zero-shot answer-letter scoring)."
            )
        stats = first.generation
        notes.append(
            f"Latency/throughput: mean over {stats.runs} runs of {stats.max_tokens} tokens "
            f"after {stats.warmup_runs} warmup run(s). Peak metal covers everything since "
            "the variant's model load; RSS is host-side only."
        )
    reasons = {
        variant.energy_unavailable_reason
        for variant in variants
        if variant.energy is None and variant.energy_unavailable_reason
    }
    for reason in sorted(reasons):
        notes.append(f"Energy sampling unavailable: {reason}.")
    if any(variant.energy is not None for variant in variants):
        notes.append(
            "Energy is system-wide CPU+GPU+ANE power over dedicated generation runs; "
            "it includes machine baseline load."
        )
    return notes


def _metric(variant: VariantResult, eval_name: str, key: str) -> float | None:
    """Metric scoped to one evaluator — several evals share key names."""
    for eval_result in variant.evals:
        if eval_result.name == eval_name:
            value = eval_result.metrics.get(key)
            return float(value) if value is not None else None
    return None


def _mb(size_bytes: int | None) -> str:
    if size_bytes is None:
        return "n/a"
    return f"{size_bytes / 1024**2:.0f} MB"
